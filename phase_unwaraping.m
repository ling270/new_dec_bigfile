clc; clear; close all;

%% =========================
% 1. 时间轴
%% =========================
t = linspace(0, 1000, 2000);
Ts = t(2) - t(1);

%% =========================
% 2. 原始相位
%% =========================
phi_raw = 3*pi - 0.006*pi .* t;

%% =========================
% 3. 缠绕相位（模拟CORDIC）
%% =========================
phi_wrap = mod(phi_raw + pi, 2*pi) - pi;

%% =========================================================
% 图1：相位缠绕
%% =========================================================
figure('Color','w','Position',[100 100 1000 500]); hold on;

plot(t, phi_raw, 'b', 'LineWidth', 3);
plot(t, phi_wrap, '--', 'Color', [0 0.6 0], 'LineWidth', 3);

yline(pi, '--k', 'LineWidth', 1);
yline(-pi, '--k', 'LineWidth', 1);

xlabel('时间');
ylabel('相位');
title('相位缠绕现象');
legend({'原始相位','缠绕相位'}, 'Location','northeast');
grid on; box on;

%% =========================================================
% 4. ⭐ 标准解缠绕（关键）
%% =========================================================
phi_unwrap = unwrap(phi_wrap);

%% =========================================================
% 5. ⭐ 标准频率估计（核心结果）
%% =========================================================
dphi = diff(phi_unwrap);
freq_est = dphi / Ts;   % 角频率估计（rad/s）

t_freq = t(1:end-1);

%% =========================================================
% 图2：解缠绕相位
%% =========================================================
figure('Color','w','Position',[200 200 1000 500]); hold on;

plot(t, phi_unwrap, 'm', 'LineWidth', 3);
plot(t, phi_raw, '--k', 'LineWidth', 1.5);

xlabel('时间');
ylabel('相位');
title('解缠绕相位（unwrap）');
legend({'解缠绕','原始相位'}, 'Location','northeast');
grid on; box on;

%% =========================================================
% 图3：频率估计结果
%% =========================================================
figure('Color','w','Position',[300 300 1000 500]); hold on;

plot(t_freq, freq_est, 'r', 'LineWidth', 3);
yline(mean(freq_est), '--k', 'LineWidth', 1);

xlabel('时间');
ylabel('频率估计 (rad/s)');
title('标准频率估计结果（unwrap + diff）');

grid on; box on;

